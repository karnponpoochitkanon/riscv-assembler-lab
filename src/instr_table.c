const Instruction *instr_table_find(const char *name) {
    if (!name) {
        return NULL;
    }

    for (size_t i = 0; i < sizeof(kInstructionTable) / sizeof(kInstructionTable[0]); ++i) {
        if (strcmp(kInstructionTable[i].name, name) == 0) {
            return &kInstructionTable[i];
        }
    }
    return NULL;
}
